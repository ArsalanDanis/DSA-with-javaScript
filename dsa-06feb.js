// # Program to check if a number is odd or even!!

function checkOddEvenNumber(number) {
    if(number%2==0){
        console.log("The given number is an Even Number.");
    }
    else(
        console.log("The given numbers is an Odd Number.")
    )
}
console.log(checkOddEvenNumber(13));

// The given numbers is an Odd Number.