// Program to check if a number is Positive,Negative or Zero !!!

function checkTheNumber(number) {
    if (number > 0) {
        console.log("The given number is a Positive Number.")
    }
    else if (number < 0) {
        console.log("The given number is a Negative Number.")
    }
    else {
        console.log("The given number is Zero.")
    }
}
checkTheNumber(156);
//  The given number is a Positive Number.
checkTheNumber(-75);
// The given number is a Negative Number.
checkTheNumber(0);
// The given number is Zero.

